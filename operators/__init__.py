# Membership Operators is used to test if a value is a member (element) of a sequence, such as a string, list, tuple, set.

fruits=["Apple", "Banana", "Orange"]
# find item in List
Result = "Apple" in fruits
print(Result)
# Checking in a string
greeting = "Hello, World!"
result = "World" in greeting
print(result)